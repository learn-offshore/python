# while loop

characters = ['h', 'a', 's', 'e', 'e', 'b', 'h', 'a', 's', 'e', 'e', 'b']

i = 0

length = len(characters) #6

while (i < length): # 0-5
    print(f'index: {i}')
    print(characters[i] + '#', '\n')
    i = i + 1

print('exited the loop')
print(i)




